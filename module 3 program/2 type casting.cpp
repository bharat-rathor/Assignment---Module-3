//. Type Conversion 
//o Write a C++ program that performs both implicit and explicit type conversions and 
//prints the results. 
//o Objective: Practice type casting in C++. 

#include<iostream>
using namespace std;
int main()
{
	int a=10;
	double b=a;
	
	cout<<"The Value Is : "<<b<<endl;
	
	double pi=3.14;
	
	int c=(int)pi;
	
	cout<<"The Value Of A Is : "<<c<<endl;
}
